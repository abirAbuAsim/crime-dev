<div id="div2" class="trans-div">
<h2>Protecting Yourself in the Community</h2>
<p>
As our population ages and the number of senior citizens continues to increase, seniors have more and more opportunities to participate in civic groups, health programs, travel activities and entertainment.
Use these tips to increase your personal security in the community.<br>
1.	Do your activities in pairs-have a companion for shopping, walking, etc.<br>
2.	Carry a small purse or bag, not one with a strap that can easily be cut or grabbed.<br>
3.	Carry your purse close to your body.<br>
4.	Don't carry credit cards you don't need or large amounts of cash.<br>
5.	When taking public transportation sit near the driver or close to an exit.<br>
6.	If possible, ask the driver to watch until you are safely in the house.<br>
7.	Always keep the doors and window locked in your car.<br>
8.	Don't leave your purse or packages on the seat beside you-put them on the floor or in your trunk.<br>
9.	Travel well lit streets and plan your route-be aware of your surroundings.<br>
10.	When returning to your car check the front and back seats before entering.
</p>
</div><br>
<div id="div3" class="trans-div">
<h2>Protecting Yourself Against Fraud and Scams</h2>
<p>
Each year billions of dollars are lost to fraudulent activities. Telemarketing, health care, home equity and home improvement fraud and identity theft are just a few of the scams conducted over the phone, by mail, through the internet and door-to-door. Seniors are often targets as criminals focus on their vulnerabilities.
Here are some guidelines to help ensure the safety of your assets.<br>
1.	Never give out your Social Security, credit card or bank account numbers.<br>
2.	Destroy personal information on documents before discarding them-use a shredder if possible.<br>
3.	Don't fall for things that sound too good to be true-money, vacations, sweepstakes prizes, health cures, or low risk/high yield investment schemes.<br>
4.	Do not agree to any home improvement or sales contract until you verify the existence and reputation of the business. <br>
5.	Have a lawyer or someone you trust examine any document before you sign it.<br>
6.	Don't give money to a charitable organization until you verify its legitimacy .<br>
7.	Never send cash in the mail, or allow anyone to pick up a check at your home.
</p>
