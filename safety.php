<h2>Protecting Yourself and Your Home</h2></b>
<P>
Seniors are living longer and more independently than ever before. We all need and want a feeling of security in the safety of our homes. Your home should be a safe haven. Most burglaries occur during daylight hours and many intruders gain access through open or poorly secured doors and windows.
Here are some precautions you can take to keep you and your home safe.<br>
1.	Install solid, easy-to-use locks on your doors and windows, including sliders and garage.<br>
2.	Use deadbolt locks on all the doors.<br>
3.	Don't hide extra keys under the doormats or in planters, etc.-leave an extra set with a neighbor or friend.<br>
4.	Install a peephole and make sure you use it.<br>
5.	Never open the door to strangers. If a stranger asks to use your phone, offer to make the call for them.<br>
6.	Ask service technicians or care providers you don't know for ID before you open the door.<br>
7.	Trim trees and shrubs to eliminate hiding places.<br>
8.	Keep your home well lit at night, inside and out.<br>
9.	Draw the curtains and blinds at night.
</p>
